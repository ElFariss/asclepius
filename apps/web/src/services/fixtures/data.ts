const makeSleepEntries = (hours: number[]): SleepEntry[] =>
  hours.map((value, index) => ({
    date: `2026-04-${String(index + 1).padStart(2, "0")}`,
    hours: value,
  }));

const makeMedication = (
  id: string,
  name: string,
  description: string,
  amount: string,
  frequency: string,
  mealTiming: "before-eating" | "after-eating",
  nextDoseAt: string,
): MedicationPlan => ({
  id,
  name,
  description,
  schedule: {
    amount,
    frequency,
    mealTiming,
    nextDoseAt,
  },
});

const makeDiet = (id: string, name: string, type: DietItem["type"]): DietItem => ({
  id,
  name,
  type,
});

